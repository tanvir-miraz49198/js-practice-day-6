// string lower case
const userName = 'Tanvir Hassan';
console.log(userName.toLowerCase())
// ans :- tanvir hassan

// String upper case
const name2 = 'sabit hasan';
const vv = name2.toUpperCase()
console.log(vv)
// ans :- SABIT HASAN

